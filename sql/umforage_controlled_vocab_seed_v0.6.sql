-- Upper Michigan Foraging Guide controlled vocabulary seed set
-- Version: v0.6
-- Shared-Supabase-safe naming with project-specific prefix.

create table if not exists public.umforage_vocab_groups_v1 (
  group_slug text primary key,
  group_label text not null,
  sort_order integer not null default 0,
  is_active boolean not null default true
);

create table if not exists public.umforage_vocab_terms_v1 (
  term_slug text primary key,
  group_slug text not null references public.umforage_vocab_groups_v1(group_slug) on delete cascade,
  term_label text not null,
  broad_type text,
  sort_order integer not null default 0,
  is_active boolean not null default true
);

create table if not exists public.umforage_vocab_synonyms_v1 (
  synonym_slug text primary key,
  term_slug text not null references public.umforage_vocab_terms_v1(term_slug) on delete cascade,
  synonym_text text not null,
  sort_order integer not null default 0
);

create index if not exists umforage_vocab_terms_group_idx_v1
  on public.umforage_vocab_terms_v1(group_slug, sort_order, term_label);

create index if not exists umforage_vocab_synonyms_term_idx_v1
  on public.umforage_vocab_synonyms_v1(term_slug, sort_order);

